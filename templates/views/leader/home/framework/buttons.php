    <h2>Кнопки</h2>

    <p class="description">
        Кнопки могут иметь разные формы и цвета в зависимости от указанных классов:
        <br>
        <br><b>button-</b> - назначив такой класс (button-primary, button-secondary и т. д.) в дополнение к <i>button</i> можно назначать кнопке цвет
        <br><b>button-outline-</b> - назначив такой класс (button-outline-primary, button-outline-secondary и т. д.) вместо <b>button-</b> в дополнение к <i>button</i> кнопка будет с обводкой
        <br><b>rounded</b> - добавив данный класс кнопка станет округленным
        <br><b>dark</b> - этот класс адаптирует кнопку под темную тему (меняется только кнопка <i>light</i> и кнопки с обводкой)
        <br>
        <br>
        Классы действуют на <b>button</b> элементы и на <b>a</b> элементы одинаково.
    </p>

